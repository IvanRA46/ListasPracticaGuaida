    private void comboPaisesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboPaisesActionPerformed
       DefaultListModel modelo = new DefaultListModel();
       listCiudad.removeAll();
       
       switch(comboPaises.getSelectedIndex()){
           case 0:
               modelo.addElement("Buenos Aires");
               modelo.addElement("Cordoba");
               modelo.addElement("Rosario");
               modelo.addElement("Mar del Plata");
               modelo.addElement("San Miguel de Tucuman");
               break;
           case 1:
               modelo.addElement("Santiago");
               modelo.addElement("Puente Alto");
               modelo.addElement("Antofagasta");
               modelo.addElement("Viña del mar");
               modelo.addElement("San Bernardo");
               break;
           case 2:
               modelo.addElement("Bogotá");
               modelo.addElement("Medellin");
               modelo.addElement("Cali");
               modelo.addElement("Barranquilla");
               modelo.addElement("Cartagena");
               break;
           case 3:
               modelo.addElement("Ciudad de México");
               modelo.addElement("Guadalajara");
               modelo.addElement("Monterrey");
               modelo.addElement("Morelia");
               modelo.addElement("Puebla");
               break;
           case 4:
               modelo.addElement("Ciudad V1");
               modelo.addElement("Ciudad V2");
               modelo.addElement("Ciudad V3");
               modelo.addElement("Ciudad V4");
               break;
           case 5:
               modelo.addElement("Ciudad S1");
               modelo.addElement("Ciudad S2");
               modelo.addElement("Ciudad S3");
               modelo.addElement("Ciudad S4");
               break;
           case 6:
               modelo.addElement("Ciudad G1");
               modelo.addElement("Ciudad G2");
               modelo.addElement("Ciudad G3");
               modelo.addElement("Ciudad G4");
               break;
           case 7:
               modelo.addElement("Ciudad P1");
               modelo.addElement("Ciudad P2");
               modelo.addElement("Ciudad P3");
               modelo.addElement("Ciudad P4");
               break;
       }
       
       listCiudad.setModel(modelo);
    }//GEN-LAST:event_comboPaisesActionPerformed

