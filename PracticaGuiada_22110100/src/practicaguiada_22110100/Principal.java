    private void btnRegistrar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrar1ActionPerformed
      String pais = comboPaises.getSelectedItem().toString();
      String ciudad = listCiudad.getSelectedValue().toString();
      txtResultado.setText(pais + "-" +ciudad + "\n");
      lista.agregar(scrollDias.getValue(), comboPaises.getSelectedItem().toString(), listCiudad.getSelectedValue().toString(), scrollNum.getValue());
    }//GEN-LAST:event_btnRegistrar1ActionPerformed

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
       if(lista.estaVacia()){
           txtDatos.setText("Sin datos registrados");
       }else{
           txtDatos.setText(lista.recorrerDestinos());
       }
    }//GEN-LAST:event_btnConsultarActionPerformed

